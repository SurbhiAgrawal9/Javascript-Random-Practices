console.log(typeof c);


//convert into float
var a = 10;
var b = "20.35";
var c = a + parseFloat(b);
console.log(c) //30.35
console.log(typeof c);


//convert into Number  for remove confusion
var a = 10;
var b = "20.35";
var c = a + Number(b);
console.log(c)  //30.35
console.log(typeof c);

